public class main {
    public static void main(String[] args) {
        
        //WHILE LOOP
        //menginisialisasikan 
        int a = 0;
        //melihat apakah variabel a bernilai true atar false
        while (a == 10) {
            System.out.println("ini while loop"); //blok kode ini akan dijalankan selama a bernilai true
            a++; //mengincrenmentkan sebanyak 1 kali
        }
        
    }
    
}
